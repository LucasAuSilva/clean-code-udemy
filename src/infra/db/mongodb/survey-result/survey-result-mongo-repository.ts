import { SurveyResultModel } from '@/domain/models/survey-result'
import { SaveSurveyResultDto } from '@/domain/usecases/survey-result/save-survey-result'
import { SaveSurveyResultRepository } from '@/data/protocols/db/survey-result/save-survey-result-repository'
import { MongoHelper } from '../helpers/mongo-helper'
import { ObjectId } from 'mongodb'

export class SurveyResultMongoRepository implements SaveSurveyResultRepository {
  async save (data: SaveSurveyResultDto): Promise<SurveyResultModel> {
    const surveyResultCollection = await MongoHelper.getCollection('surveyResults')
    const surveyResult = await surveyResultCollection.findOneAndUpdate({
      surveyId: new ObjectId(data.surveyId),
      accountId: new ObjectId(data.accountId)
    }, {
      $set: {
        answer: data.answer,
        date: data.date
      }
    }, {
      upsert: true,
      returnDocument: 'after',
      projection: {
        _id: 1,
        surveyId: 1,
        accountId: 1,
        answer: 1,
        date: 1
      }
    })
    return surveyResult.value && MongoHelper.map<SurveyResultModel>(surveyResult.value)
  }
}
